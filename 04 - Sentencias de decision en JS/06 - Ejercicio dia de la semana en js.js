// Dia de la semana en Javascript (1 a 7 dias)

let diaSemana = 1;

if (diaSemana == 1)
    console.log(`Es lunes`);
else if (diaSemana == 2)
    console.log(`Es martes`);
else if (diaSemana == 3)
    console.log(`Es miercoles`);
else if (diaSemana == 4)
    console.log(`Es jueves`);
else if (diaSemana == 5)
    console.log(`Es viernes`);
else if (diaSemana == 6)
    console.log(`Es sabado`);
else if (diaSemana == 7)
    console.log(`Es domingo`);
else
    console.log(`Dia de la semana invalido ${diaSemana}`);