let a = true;
let b = false;
console.log(a);
console.log(b);

// Operador logico && (AND o Y)
// Devuelve true si ambos operadores son true
console.log(` ${a} && ${b} -> ${a && b}`);

// Operador logico || (OR u o)
// Devuelve true si uno de los operadores es true
console.log(` ${a} || ${b} -> ${a || b}`);

// Operador logico ! (invetir)
// Invierte el valor del operador
console.log(`!${a} -> ${!a}`);