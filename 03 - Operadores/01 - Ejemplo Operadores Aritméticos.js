// Operadores Aritméticos

let a, b, c, d, e ,f;

// Suma
a = 3 + 4;
console.log(a);

// Resta
b = 10 - 5;
console.log(b);

// Multiplicacion
c = a * 5;
console.log(c);

// División
d = b / 2.5;
console.log(d);

// Módulo
e = d % 2;
console.log(e);

// Potencia (ES2016 - ECMAScript 2016)
f = 10 ** 2; // 10 * 10
console.log(f);

// Anterior a ES2016
f = Math.pow(10,2);
console.log(f);