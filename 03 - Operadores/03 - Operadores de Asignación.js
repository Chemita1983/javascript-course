// Operadores de asignación
let miNumero = 10; // si no se le asigna un valor, el valor será undefined
console.log(miNumero);

miNumero = 20;
console.log(miNumero);

// Operador +=
miNumero += 5;
console.log(miNumero);

// Operador -=
miNumero -= 3;
console.log(miNumero);

// Operador *= 
miNumero *= 2;
console.log(miNumero);

// Operador /= 
miNumero /= 4;
console.log(miNumero);

// Operador %=
miNumero %= 2; 
console.log(miNumero);

/* 
11 : 5 = 
11 - (5 x 2) = 
11 - 10 = 1 
*/

// Operador **=
miNumero = 8;
miNumero **= 2;
console.log(miNumero);