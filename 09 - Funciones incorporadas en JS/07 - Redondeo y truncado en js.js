// Redondeo y truncado de numeros en JS

// Redondeo
let numero = 5.7897;
console.log(Math.round(numero));

numero = 5.3333;
console.log(Math.round(numero));

numero = 5.499999
console.log(Math.round(numero));

// Truncado
numero = 5.499999
console.log(Math.trunc(numero,));

numero = 5.9999
console.log(Math.trunc(numero,));

